package examen.services;

public class FutbolXmlException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -823257359280364198L;

	public FutbolXmlException() {
	}

	public FutbolXmlException(String message) {
		super(message);
	}

	public FutbolXmlException(Throwable cause) {
		super(cause);
	}

	public FutbolXmlException(String message, Throwable cause) {
		super(message, cause);
	}

	public FutbolXmlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
